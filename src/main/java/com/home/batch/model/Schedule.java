package com.home.batch.model;

public class Schedule {

	private String state;
	private String carrier;
	private String locality;
	private String ruralInd;
	private String labLocality;
	private String plusFlag;
	private String partBInd;
	private String year;
	
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getCarrier() {
		return carrier;
	}
	public void setCarrier(String carrier) {
		this.carrier = carrier;
	}
	public String getLocality() {
		return locality;
	}
	public void setLocality(String locality) {
		this.locality = locality;
	}
	public String getRuralInd() {
		return ruralInd;
	}
	public void setRuralInd(String ruralInd) {
		this.ruralInd = ruralInd;
	}
	public String getLabLocality() {
		return labLocality;
	}
	public void setLabLocality(String labLocality) {
		this.labLocality = labLocality;
	}
	public String getPlusFlag() {
		return plusFlag;
	}
	public void setPlusFlag(String plusFlag) {
		this.plusFlag = plusFlag;
	}
	public String getPartBInd() {
		return partBInd;
	}
	public void setPartBInd(String partBInd) {
		this.partBInd = partBInd;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}

}
